function nav(obj) {
    var objName = obj.value;
    if (objName == "Home") {
        window.open("http://blog.dev2014.com/");
    }
    if (objName == "Hardware") {
        window.open("hardware.html");
    }
    if (objName == "Software") {
        window.open("software.html");
    }
    if (objName == "Album") {
        window.open("http://www.dev2014.com/album.html");
    }
    if (objName == "Music") {
        window.open("http://www.dev2014.com/music.html");
    }
}